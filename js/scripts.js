$(document).ready(function() {
  $("#design-pic").click(function() {
    $(".design-text").toggle();
    $("#design-pic").toggle();
  });
  $(".design-text").click(function() {
    $("#design-pic").show();
    $(".design-text").hide();
  })
  $("#development-pic").click(function() {
    $(".development-text").toggle();
    $("#development-pic").toggle();
  });
  $(".development-text").click(function() {
    $("#development-pic").show();
    $(".development-text").hide();
  })
  $("#product-pic").click(function() {
    $(".product-text").toggle();
    $("#product-pic").toggle();
  });
  $(".product-text").click(function() {
    $("#product-pic").show();
    $(".product-text").hide();
  })
});
